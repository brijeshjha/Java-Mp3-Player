    public void startplaylist() throws InterruptedException          
    {
        DefaultTableModel model1=(DefaultTableModel)tb1.getModel();
        if(playrow<rowCount)
        {   playrow++;
            if(playrow==rowCount)
            {
               playrow=0; 
            }
            MC.stop();
            trackdest=model1.getValueAt(playrow,1).toString();
            trackname=model1.getValueAt(playrow,0).toString();
            MC.play(trackdest);           
            System.out.println("next = "+playrow);
        }
    }
    
    
  public void backplay()
  {
      
      DefaultTableModel mymodel=(DefaultTableModel)tb1.getModel();
        if(playrow>=0)
        {   
            if(playrow>0)
            {
            playrow--;
            MC.stop();
            trackdest=mymodel.getValueAt(playrow,1).toString();
            trackname=mymodel.getValueAt(playrow,0).toString();
            MC.play(trackdest); 
            }
            else
            {
                playrow=rowCount-1;
                MC.stop();
                trackdest=mymodel.getValueAt(playrow,1).toString();
                trackname=mymodel.getValueAt(playrow,0).toString();
                MC.play(trackdest);
                
            }
            System.out.println("back next = "+playrow);
        }
  }
  
  

    
    
