  private void animatee() {
        
        new Thread()
        {
            
            public void run()
            {
                int count=0;
                try
                {
                    
                    while(true)
                    {
                        switch(count)
                        {
                            case 0:
                                     ac.jLabelXLeft(0,-300,20,1,label1);
                                     count=1;
                                     break;
                            case 1: Thread.sleep(300);
                                     ac.jLabelXRight(-300,0,20,1,label1);
                                     count=0;
                                     break;
                        }
                    }
                    
                    
                }catch(Exception e)
                {
                    System.out.println("ERROR!"+e);
                }
            }
            
        }.start();
                
    }
